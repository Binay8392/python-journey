import math

print(round(3.14159, 2)) #rounding to 2 decimal places

print(abs(-5)) #absolute value of -5

print(math.ceil(3.14159)) #rounding up to the nearest integer

print(math.floor(3.14159)) #rounding down to the nearest integer

print(math.trunc(3.14159)) #truncating the decimal part

print(math.fabs(-5)) #absolute value using math module

print(math.factorial(5)) #factorial of 5

print(math.isqrt(16)) #integer square root of 16

print(math.log(100, 10)) #logarithm of 100 to the base 10

print(math.sin(math.radians(30))) #sine of 30 degrees

print(math.lcm(12, 15)) #least common multiple of 12 and 15


print(math.sqrt(16)) #square root of 16

print(math.pow(2, 3)) #2 raised to the power of 3  

print(math,perm(5, 2)) #permutation of 5 items taken 2 at a time
print(math.comb(5, 2)) #combination of 5 items taken 2 at a time

print(math.fmod(5, 2)) #floating point modulo of 5 and 2
